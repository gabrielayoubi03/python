# Useful built-in and imported functions for math in Python
import math

pi = 3.14
x = 1
y = 2
z = 3

print("Pi rounded is " + str(round(pi)))
print("Pi rounded up is " + str(math.ceil(pi)))
print("The absolute value of pi is " + str(abs(pi)))
print("Pi squared is " + str(math.pow(pi, 2)))
print("The square root of pi is " + str(math.sqrt(pi)))
print(max(x, y, z))
print(min(x, y, z))